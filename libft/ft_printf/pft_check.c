#include "../includes/ft_printf.h"

int		pft_isflag(char c, t_pf *p)
{
	if (c == 'c' || c == 's' || c == 'd' || c == 'i' || c == 'o' || c == 'p'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
	{
		p->flag = c;
		return (1);
	}
	return (0);
}

int		pft_isoption(char c)
{
	if ((c >= '0' && c <= '9') || c == ' ' || c == '+' || c == '-'
		|| c == 'h' || c == 'l' || c == 'L' || c == '#' || c == '.')
		return (1);
	return (0);
}
